# 1.Arithmetic Operators
num1 = 40
num2 = 20

print(num1 - num2) 
print(num1 * num2)

# 2.Assignment Operators
value = 50 
print(value)

value = -10
print(value) # subtract and assign

# 3. Comparison Operators
p1 = 15 
p2 = 15
print(p1 > p2) # greater than
print(p1 < p2) # less than

# 4. Logical Operators
x1 = 5 
x2 = 15 
x3 = 25
x4 = 35

print(x1 < x2 and x3 < x4) # Logical AND
print(x1 < x2 or x3 > x4) # Logical OR

# 5. Identity Operators
a1 = 10
a2 = 10
print(a1 is a2) # Output: True

x1 = 5
x2 = 15
print(x1 is not x2) # Output: True

# 6. Membership Operators not in, in
print("a" in "python") # Output: False
print("o" in "python") # Output: True

print("check 'e' in 'Welcome' ", 'e' in 'Welcome') # Output: False